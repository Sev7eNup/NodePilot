    [Fact]
    public async Task RegisterDaily_BuildsActionTriggerPrincipalSettings()
    {
        var act = CreateActivity();
        await act.ExecuteAsync(Ctx(), Cfg(@"{
            ""action"":""register"",
            ""taskName"":""Nightly"",
            ""program"":""C:\\Tools\\backup.exe"",
            ""arguments"":""-quiet"",
            ""triggerType"":""daily"",
            ""startTime"":""02:00"",
            ""runAsUser"":""SYSTEM"",
            ""runLevel"":""highest"",
            ""description"":""Nightly backup"",
            ""force"":true
        }"), CancellationToken.None);
        _capturedScript.Should().Contain("$__npAction = New-ScheduledTaskAction -Execute 'C:\\Tools\\backup.exe'");
        _capturedScript.Should().Contain("-Argument '-quiet'");
        _capturedScript.Should().Contain("$__npTrigger = New-ScheduledTaskTrigger -Daily -At '02:00'");
        _capturedScript.Should().Contain("$__npPrincipal = New-ScheduledTaskPrincipal -UserId 'SYSTEM' -RunLevel Highest");
        _capturedScript.Should().Contain("Register-ScheduledTask");
        _capturedScript.Should().Contain("-Action $__npAction -Trigger $__npTrigger -Principal $__npPrincipal -Settings $__npSettings");
        _capturedScript.Should().Contain("-Description 'Nightly backup'");
        _capturedScript.Should().Contain("-Force");
        _capturedScript.Should().Contain("$ErrorActionPreference = 'Stop'");
        _capturedScript.Should().NotContain("Schedule.Service");
    }

